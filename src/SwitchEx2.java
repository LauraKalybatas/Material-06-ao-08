public class SwitchEx2 {
    public static void main(String[] args) {
        int valor = 4;

        switch (valor) {
            case 1, 2 -> System.out.println("bdsmer");
            case 3, 4 -> System.out.println("bodosamer");
            case 5, 6 -> System.out.println("babysaja");
            default -> System.out.println("defo");
        }
    }
}
